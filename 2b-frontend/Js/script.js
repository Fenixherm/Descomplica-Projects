const num1 = 25;
const num2 = 9;

const cossenoNum1 = Math.cos(num1);
const maxNum = Math.max(num1, num2);
const minNum = Math.min(num1, num2);
const potenciaNum1 = Math.pow(num1, 2);

console.log(`Cosseno de ${num1} é: ${cossenoNum1}`);
console.log(`O maior número entre ${num1} e ${num2} é: ${maxNum}`);
console.log(`O menor número entre ${num1} e ${num2} é: ${minNum}`);
console.log(`${num1} elevado ao quadrado é: ${potenciaNum1}`);